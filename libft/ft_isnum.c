#include "libft.h"

int	ft_isnum(char *str, int c)
{
	size_t	i;

	i = 0;
	if (!str[i])
		return (0);
	while (str[i] != c)
	{
		if (!ft_isdigit(str[i]))
			return (0);
		++i;
	}
	return (1);
}
