#include "libft.h"

size_t	ft_lstsize(t_list *lst)
{
	size_t s;
	t_list *tmp;

	s = 0;
	if (lst)
	{
		tmp = lst;
		while (tmp)
		{
			tmp = tmp->next;
			s++;
		}
	}
	return (s);
}
